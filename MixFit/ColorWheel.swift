import Foundation
import UIKit

struct ColorWheel {

    static func leadColor() -> UIColor {
        return UIColor(red: 0.098, green: 0.098, blue: 0.098, alpha: 1.0)
    }

    static func redColor() -> UIColor {
        return UIColor(red: 203.0 / 255.0, green: 51.0 / 255.0, blue: 0, alpha: 1.0)
    }


}
